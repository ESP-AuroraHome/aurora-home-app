#!/usr/bin/env node
/**
 * display-otp.mjs — Drive the GME12864 128x64 I2C display (SSD1306 controller)
 * to show or clear an OTP code.
 *
 * Usage:
 *   node display-otp.mjs show <otp> <email>
 *   node display-otp.mjs clear
 *
 * Environment variables:
 *   DISPLAY_OTP_I2C_BUS      I2C bus number (default: 0)
 *   DISPLAY_OTP_I2C_ADDRESS  I2C address in hex string (default: "0x3C")
 *
 * Dependencies:
 *   i2c-bus (optionalDependency — run `npm install i2c-bus` if missing)
 */

let i2cBus;
try {
  i2cBus = await import("i2c-bus");
} catch {
  console.error(
    [
      "",
      "  ✗ i2c-bus is not installed.",
      "    Run the following on your OrangePi:",
      "",
      "      sudo apt install build-essential python3 make g++",
      "      npm install i2c-bus",
      "",
    ].join("\n"),
  );
  process.exit(1);
}

const { openSync } = i2cBus;

const I2C_BUS = parseInt(process.env.DISPLAY_OTP_I2C_BUS ?? "0", 10);
const I2C_ADDRESS = parseInt(process.env.DISPLAY_OTP_I2C_ADDRESS ?? "0x3C", 16);

// ─── SSD1306 helpers ────────────────────────────────────────────────────────

const CMD = 0x00;
const DATA = 0x40;

function sendCmd(bus, ...cmds) {
  for (const cmd of cmds) {
    bus.writeByteSync(I2C_ADDRESS, CMD, cmd);
  }
}

function sendData(bus, bytes) {
  const buf = Buffer.from(bytes);
  // Write in chunks of 32 bytes (safe for all I2C implementations)
  for (let i = 0; i < buf.length; i += 32) {
    const chunk = buf.slice(i, i + 32);
    const payload = Buffer.concat([Buffer.from([DATA]), chunk]);
    bus.i2cWriteSync(I2C_ADDRESS, payload.length, payload);
  }
}

function initDisplay(bus) {
  sendCmd(
    bus,
    0xae, // display off
    0xd5,
    0x80, // clock divide ratio
    0xa8,
    0x3f, // multiplex ratio (64 rows)
    0xd3,
    0x00, // display offset
    0x40, // start line
    0x8d,
    0x14, // charge pump on
    0x20,
    0x00, // horizontal addressing mode
    0xa1, // segment remap (column 127 → SEG0, flip horizontal)
    0xc8, // COM output scan direction
    0xda,
    0x12, // COM pins hardware config
    0x81,
    0xcf, // contrast
    0xd9,
    0xf1, // pre-charge period
    0xdb,
    0x40, // VCOMH deselect level
    0xa4, // entire display on (use RAM)
    0xa6, // normal display (not inverted)
    0xaf, // display on
  );
}

function setPage(bus, page) {
  sendCmd(
    bus,
    0xb0 | (page & 0x07), // page address
    0x00, // lower column start
    0x10, // upper column start
  );
}

function clearDisplay(bus) {
  const blank = new Array(128).fill(0);
  for (let p = 0; p < 8; p++) {
    setPage(bus, p);
    sendData(bus, blank);
  }
}

