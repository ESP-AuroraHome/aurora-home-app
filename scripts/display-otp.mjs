const FALLBACK = [0x00, 0x00, 0x3c, 0x3c, 0x3c, 0x3c, 0x00, 0x00];

/** Render a string into a flat array of column bytes (8px tall, 8px wide per char). */
function renderText(text) {
  const cols = [];
  for (const ch of text) {
    const glyph = FONT[ch] ?? FALLBACK;
    // Each glyph byte is a row; we need to transpose to columns for SSD1306 writes.
    // The bitmap table uses LSB-first rows.
    for (let col = 7; col >= 0; col--) {
      let colByte = 0;
      for (let row = 0; row < 8; row++) {
        if (glyph[row] & (1 << col)) colByte |= 1 << row;
      }
      cols.push(colByte);
    }
    cols.push(0x00); // 1px spacing between characters
  }
  return cols;
}

/** Write a text line to a given page, padded/truncated to 128 columns. */
function _writeLine(bus, page, text, invert = false) {
  setPage(bus, page);
  let cols = renderText(text);
  // Pad or truncate to exactly 128 columns
  if (cols.length < 128)
    cols = cols.concat(new Array(128 - cols.length).fill(0));
  else cols = cols.slice(0, 128);
  if (invert) cols = cols.map((b) => ~b & 0xff);
  sendData(bus, cols);
}

/** Write a text line scaled 2× tall, occupying two consecutive pages. */
function writeLineLarge(bus, startPage, text) {
  // Compute columns at normal size first
  const cols = renderText(text);
  // Centre horizontally
  const totalWidth = cols.length;
  const padLeft = Math.max(0, Math.floor((128 - totalWidth) / 2));
  const padded = new Array(padLeft).fill(0).concat(cols);
  if (padded.length < 128)
    padded.push(...new Array(128 - padded.length).fill(0));
  const paddedSlice = padded.slice(0, 128);

  // Top page: lower 4 bits of each column byte → upper nibble scaled
  const topRow = paddedSlice.map((b) => {
    let out = 0;
    for (let bit = 0; bit < 4; bit++) {
      if (b & (1 << bit)) out |= 0x03 << (bit * 2);
    }
    return out & 0xff;
  });
  // Bottom page: upper 4 bits
  const botRow = paddedSlice.map((b) => {
    let out = 0;
    for (let bit = 4; bit < 8; bit++) {
      if (b & (1 << bit)) out |= 0x03 << ((bit - 4) * 2);
    }
    return out & 0xff;
  });

  setPage(bus, startPage);
  sendData(bus, topRow);
  setPage(bus, startPage + 1);
  sendData(bus, botRow);
}

// ─── Commands ───────────────────────────────────────────────────────────────

function showOTP(otp) {
  const bus = openSync(I2C_BUS);
  try {
    initDisplay(bus);
    clearDisplay(bus);
    writeLineLarge(bus, 3, otp); // pages 3-4: OTP x2 size, centered
  } finally {
    bus.closeSync();
  }
}

function clearScreen() {
  const bus = openSync(I2C_BUS);
  try {
    initDisplay(bus);
    clearDisplay(bus);
  } finally {
    bus.closeSync();
  }
}

// ─── Entry point ────────────────────────────────────────────────────────────

const [, , command, ...rest] = process.argv;

if (!command) {
  console.error(
    "Usage: display-otp.mjs show <otp> <email> | display-otp.mjs clear",
  );
  process.exit(1);
}

if (command === "show") {
  const [otp, email] = rest;
  if (!otp || !email) {
    console.error("Usage: display-otp.mjs show <otp> <email>");
    process.exit(1);
  }
  showOTP(otp, email);
} else if (command === "clear") {
  clearScreen();
} else {
  console.error(`Unknown command: ${command}`);
  process.exit(1);
}
